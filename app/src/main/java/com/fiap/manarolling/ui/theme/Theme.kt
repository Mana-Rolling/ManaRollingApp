@Composable
fun ManaRollingAppTheme( // Renomeie para o nome do seu aplicativo + Theme
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color é disponível no Android 12+
    // Para este exemplo, vamos desabilitar por padrão, mas você pode habilitá-lo.
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    // Código para controlar a aparência da barra de status e de navegação (opcional, mas bom para consistência)
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb() // Cor de fundo do tema
            window.navigationBarColor = colorScheme.background.toArgb() // Cor de fundo do tema

            // Controla se os ícones da barra de status e navegação devem ser escuros ou claros
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
//        typography = Typography, // Assumindo que você tem Typography.kt definido
//        shapes = Shapes,         // Assumindo que você tem Shapes.kt definido
        content = content
    )
}